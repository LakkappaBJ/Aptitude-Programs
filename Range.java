//To calculate range of datatypes
class Range{
	public static void main(String args[]){
	System.out.println(Byte.MAX_VALUE); //Byte wrapper class
	System.out.println(Byte.MIN_VALUE);
	System.out.println(Short.MAX_VALUE); //Short wrapper class
	System.out.println(Short.MIN_VALUE);
	System.out.println(Integer.MAX_VALUE); //Integer wrapper class
	System.out.println(Integer.MIN_VALUE);
	System.out.println(Long.MAX_VALUE); //Long wrapper class
	System.out.println(Long.MIN_VALUE);
	System.out.println(Float.MAX_VALUE);  //Float wrapper class
	System.out.println(Float.MIN_VALUE);
	System.out.println(Double.MAX_VALUE); // Double wrapper class
	System.out.println(Double.MIN_VALUE);
	}
}
